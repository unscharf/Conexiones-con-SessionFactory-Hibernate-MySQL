    public Inicio(SessionFactory sf, String user, Date d) {
        
        initComponents();
        this.sf=sf;
        this.user=user;
        this.d= d;
      //Guardar sessión en tabla prueba
        try{
        st= sf.openSession();
        st.beginTransaction();
        
        Prueba p= new Prueba();
        p.setSession(user);
        p.setFecha(d);
        st.save(p);
        st.getTransaction().commit();
        }catch(HibernateException ee){}
        
        //Identificadores de columna
        dtm.setColumnIdentifiers(new Object[]{"ID","Sessión", "Última conexión"});
        jTable1.setModel(dtm);
        //Añadiendo Row sorter
        TableRowSorter sorter = new TableRowSorter(dtm);
        jTable1.setRowSorter(sorter);
        setLocationRelativeTo(null);
        CargarSessiones();
    }
