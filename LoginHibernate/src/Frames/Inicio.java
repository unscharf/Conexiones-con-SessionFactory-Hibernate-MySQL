    public void CargarSessiones(){
    st= sf.openSession();
    Criteria criteria = st.createCriteria(Prueba.class);
    int i=0;
    
 //*Agregando datos de result set Criteria
    for(Object Prueba: criteria.list()){
    Prueba p= (Prueba) Prueba;
    dtm.addRow(new Object[]{});
    dtm.setValueAt(p.getId(),i,0);
    dtm.setValueAt(p.getSession(),i,1);
    dtm.setValueAt(p.getFecha(),i,2);
    i++;
    }//
    st.close();
    }
    
